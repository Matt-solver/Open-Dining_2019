//card button
$('#a_card0').click(function() {
	var cardName = $('.a_card0').val()
	alert(cardName);
	$('.a_card').css('background-color', 'white');
	$('.a_card').css('color', 'gray');
	$('.b_card').css('background-color', 'white');
	$('.b_card').css('color', 'gray');	
	
	$('#a_card0').css('background-color', '#408080');
	$('#a_card0').css('color', 'white');
	
	$('#cardName').val(cardName);
	$('.cardName').text(cardName);
	$('.creditCardFirm').val(cardName);
});
